    private void videoStopped() {
        mChronometer.stop();
        mChronometer.setVisibility(View.INVISIBLE);
        mIsRecording = false;
        mIsTimelapse = false;
        mIsSlowmo = false;
        mRecordBtn.setImageResource(R.drawable.ic_recording_start);

        if(!mIsTimelapse && !mIsSlowmo) mPauseBtn.setVisibility(View.GONE);

        // Starting the preview prior to stopping recording which should hopefully
        // resolve issues being seen in Samsung devices.
        startPreview();
        mMediaRecorder.stop();
        mMediaRecorder.reset();

        Intent mediaStoreUpdateIntent = new Intent(Intent.ACTION_MEDIA_SCANNER_SCAN_FILE);
        mediaStoreUpdateIntent.setData(Uri.fromFile(new File(mVideoFileName)));
        sendBroadcast(mediaStoreUpdateIntent);

        if (isShare) {
            Intent toShare = new Intent(mContext, ShareActivity.class);
            toShare.putExtra("video_path", mVideoFileName);
            startActivity(toShare);
            finish();
        } else if (isStory){
            Intent toStory = new Intent(mContext, StoryActivity.class);
            toStory.putExtra("video_path", mVideoFileName);
            startActivity(toStory);
            finish();
        } else {
            Intent toReply = new Intent(mContext, ReplyActivity.class);
            toReply.putExtra("video_path", mVideoFileName);
            toReply.putExtra("chain_id", chain_id);
            toReply.putExtra("creator_id", creator_id);
            startActivity(toReply);
            finish();
        }
    }

